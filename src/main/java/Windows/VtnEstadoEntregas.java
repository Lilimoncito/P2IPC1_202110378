    private void enviarMoto(String motoNombre, JLabel motoLabel) {
    Pedido pedido = new Pedido();
    boolean pedidoEncontrado = false;
    
    for (Pedido pedidoBus : datosGlobales.getPEDIDOS()) {
        if (pedidoBus.getVehiculoName().equals(motoNombre) && !pedidoBus.isPedidoFinalizado()) {
            pedido = pedidoBus;
            motoLabel.setLocation(37, motoLabel.getLocation().y);
            RecorridoGrafico recorrido = new RecorridoGrafico(motoLabel, this, pedidoBus);
            recorrido.start();
            pedidoEncontrado = true;
            break;
        }
    }
    
    if (!pedidoEncontrado) {
        JOptionPane.showMessageDialog(this, "No hay ning√∫n pedido asociado a esta moto"+" "+motoNombre);
    }
}
